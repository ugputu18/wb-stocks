export const UNMAPPED_WAREHOUSE_REGION_LABEL = "Не сопоставлен";

function normalizeWarehouseKey(raw: string | null | undefined): string {
  if (raw == null) return "";
  return String(raw)
    .replace(/\u00A0/g, " ")
    .replace(/\s+/g, " ")
    .trim()
    .toLocaleLowerCase("ru-RU");
}

